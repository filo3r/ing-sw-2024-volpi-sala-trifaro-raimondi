package it.polimi.ingsw.gc03.model.enumerations;

/**
 * Enumeration representing the different types of decks in the game.
 */
public enum DeckType {

    /**
     * Displayed Gold deck type.
     */
    DISPLAYED_GOLD,

    /**
     * Displayed Resource deck type.
     */
    DISPLAYED_RESOURCE,

    /**
     * Gold deck type.
     */
    DECK_GOLD,

    /**
     * Resource deck type.
     */
    DECK_RESOURCE,

    /**
     * Starter deck type.
     */
    DECK_STARTER,

    /**
     * Objective deck type.
     */
    DECK_OBJECTIVE

}