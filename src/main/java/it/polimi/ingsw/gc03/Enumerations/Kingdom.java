package it.polimi.ingsw.gc03.Enumerations;

/**
 * This enumeration class represents the available kingdoms in the game.
 * Resource cards and Gold cards can belong to one of the kingdoms defined here.
 */
public enum Kingdom {

    /**
     * Fungi kingdom.
     */
    FUNGI,

    /**
     * Plant kingdom.
     */
    PLANT,

    /**
     * Animal kingdom.
     */
    ANIMAL,

    /**
     * Insect kingdom.
     */
    INSECT
}
