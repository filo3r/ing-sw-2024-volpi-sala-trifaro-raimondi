package it.polimi.ingsw.gc03.view;

/**
 * The OptionSelection enum represents different options for user interface and connection types.
 */
public enum OptionSelection {

    /**
     * Text-based User Interface option.
     */
    TUI,

    /**
     * Graphical User Interface option.
     */
    GUI,

    /**
     * Remote Method Invocation option.
     */
    RMI,

    /**
     * Socket-based connection option.
     */
    SOCKET

}